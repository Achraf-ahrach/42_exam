#include <stdio.h>
#include <unistd.h>

int main(int ac, char **av)
{
	int i = 0;
	int j = 0;

	if (ac == 2)
	{
		while (av[1][i])
			i++;
		i--;
		while (i > 0)
		{
			j = i;
			while (av[1][j] != ' ' && j >= 0)
				j--;
			i = j + 1;
			av[1][j] = '\0';
			j--;
			while (av[1][i])
				write (1, &av[1][i++], 1);
			i = j;
			if (j > 0)
				write (1, " ", 1);
		}
	}
	write (1, "\n", 1);
}
