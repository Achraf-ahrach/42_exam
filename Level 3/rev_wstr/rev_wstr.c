#include <unistd.h>
#include <stdio.h>

int main(int ac, char **av)
{
	int i;
	int j;
	int x;

	i = 0;
	j = 0;
	if (ac == 2)
	{
		while (av[1][i])
			i++;
		while (i >= 0)
		{
			if (av[1][i] == ' ')
				{
				   j = i;
				   while (av[1][i] != '\0')
					   write(1, &av[1][i++], 1);
				   write (1, "", 1);
				   i = j;
				   av[1][i] = '\0';
				}
			i--;
		}
	}
	write (1, "\n", 1);
}
